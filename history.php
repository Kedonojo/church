            <h2>Our History</h2>

            <p>The Presbyterian Church of Nigeria is aware of its origins in the Reformation, especially for the enlightening ministries of John Calvin in Switzerland and John Knox in Scotland.<br>



<br>The Presbyterian Church of Nigeria is also indebted to the Presbyterian Church in Jamaica and the Jamaican Missionary Board for West Indies and Africa for taking the initiative to send five Jamaicans and the Rev. Hope Masterdon Waddell to Calabar in 1846 at the invitation of King Evamba V and King Eyo II and other chiefs of Calabar. The Presbyterian Church of Nigeria is also grateful to the Church of Scotland Mission for its generous support of further mission personnel bringing in the light of the gospel.

The Presbyterian Church of Nigeria remembers the creation of the Presbytery of Biafra for the oversight of the congregations formed and Creek Town and Duke Town on September 1, 1858, and to be known as “The Presbyterian Church in Biafra.” The Church gives thanks to God for the memory of its first indigenous Nigerian minister, the Rev. Esien Esien Ukpabio, ordained to the holy ministry by the Presbytery of Biafra on April 9, 1872. The Church still rejoices when on May 4, 1921 the Synod of Biafra was formed, comprised of two Presbyteries. The Synod became “The Presbyterian Church of Eastern Nigeria” in June, 1952, and in recognition of its own national character and mission, in June 1960 changed its name to “The Presbyterian Church of Nigeria”.

<br>The Presbyterian Church of Nigeria gives glory to God and expresses its everlasting gratitude to the Church of Scotland, who already in 1945 began a plan of handing over to indigenous Nigerians that part of its Church established on Nigerian soil, to become a self governing and independent Nigerian counterpart to the Church of Scotland. This handing over was completed by 1960.

Not to be forgotten is this Church’s strong determination to remain a single entity, even through the ravages of the Nigerian Civil War of 1967 1970, and hence, on the 7th of January, 1971, the Presbyterian Church of Nigeria resolved that for “the furtherance of evangelism and its mission in the nation, to adopt a new structure which includes a General Assembly as he supreme court of the Church, as well as Synods, Presbyteries, and Sessions.”

In the light of this historical context, the Presbyterian Church of Nigeria remains faithful to its origins, yet equally faithful to the leading of God through His Holy Spirit in adapting itself to contemporary situations.

<br>First Abuja Parish
First Abuja Parish of the Presbyterian Church of Nigeria, as the name implies, is the first Presbyterian Church in the Federal Capital Territory, Abuja.



The idea of establishing a Presbyterian Church in Abuja was first muted during the 1978 Annual Synod (now General Assembly) meeting. This was one of the reactions of the Church to the Federal Government of Nigeria’s decision to relocate her national capital from Lagos to Abuja in 1976. In order to foreclose the possibility of our Church being a late entrant to Abuja as was the case in Lagos, the machinery was immediately set in motion. 1983, the Rev. E. A. Ndon was posted to formally commence the Church.

<br>In August 21, 1983, the Rev. Ndon organized the first divine worship service in the living room of one Mr. Ufiem. A. Oji at Block 9, Flat 9, Area 11, Garki, Abuja. This preaching post was erected as an Extension Charge on Sunday April 1, 1984, during a divine service at Old Parade Ground, Abuja. Three years after, on Sunday July 26th, 1987, the Extension Charge was formally given a full Parish Status with the ordination of two pioneer Elders, namely Agbai Onuoha Ogba and Ifechide Kalu Umoga in the same service of inauguration.

Since then, the Parish has grown from strength to strength, giving birth to many other Parishes within the FCT and its environs. The Parishes are Nyanya, Kubwa, and Minna all inaugurated in 1995. Other new Parishes that have emerged from the First Abuja Parish include Gwagwalada (1996); defunct Karmo (2004), Lugbe (2005) and Gwarinpa (2010). Mpape and Zauda congregations are looking forward to attaining Parish status soon.

<br>The following Ministers have served as Associate Ministers of the Parish in chronological order:
Revs Marshal Kalu, Nene Amogu, Ambrose Onyobi, Effiom Opla, Ume Okorie, Lekwauwa Eke, Mrs. Ntonova Nkanu, Major Nze, Mrs. Uzoma Dike, Precious Ogo, Grace Oku, Emeka Kalu and Jessica Okon-Effiong. Revs Nene Amogu, Ebere Solomon-Umazi, Ijeoma Okoronkwo, Inyang Ubi and Emmanuel Nwankwo are currently (2013) serving as Associate ministers.

The Kirk Session has about 30 active Ruling Elders on roll, with Elder Ifendu K. Ukonu as incumbent Session Clerk and Elder Dr. E. E. Nkang as incumbent Board Chairman.</p>

<a id="st-of-faith"> <h1>Statement Of Faith</h1></a>
          
           
<p>These are the statements of Bible doctrine as believed and taught by the PRESBYTERIAN Church of Nigeria. Each of these statements of faith has been directly deduced from the Bible. We believe they are agreeable with any unbiased interpretation of the Bible. They constitute for us the fundamental doctrines and tenets of the Christian faith.<br>

As Presbyterians we believe that the whole Bible (Old and New Testaments) is the inspired and infallible word of God (2 Timothy 3:16, 2 Peter 1:20-21). It remains the primary basis of all we are and do. This is reflected in the prime and central position of the Bible in worship liturgy, namely: an open Bible leads the procession. It is for us a spiritual sword (Ephesians 6:17).<br>
While depending on the Bible, Presbyterians believe that there is only one holy, invisible God who eternally and actively exists in three persons: God the Father, God the Son and God the Holy Ghost. (Gen. 1:1, 26; Ephesians 4:5-6; Matthew 3:16-17). This God is spirit (John 4:24), Creator and owner of all things (Gen. 1:1; Psalm 24:1); Love (1 John 4:8, 16; John 3:16); Self revealing (1 John 1:5); and Judge (Gen. 18:25; Deut. 4:24 cf. Hebrew 12:29)
We believe the humankind is the pinnacle of God’s creation (Gen. 1:26-28), superior to all other creatures (Gen. 1:28; Matt. 10; 31), made solely for God’s pleasure (Isaiah 43:7; Rev. 4:11) but fallen (Rom. 3:23; Gen. 2:16-17;3:1-6) and spiritually dead (Ephesians 2:1); in need of salvation through God’s love and grace (John 3:16; Rom. 5:6-10; Ephesians 2:8-9)<br>
We believe in the deity and humanity of Jesus Christ. (John 1:1-4, 14; John 10:30; Hebrews 1:1-5).<br>
He was born of a virgin (Matthew 1:18-25; Luke 1:30-35);<br>
He was conceived of the Holy Ghost (Matthew 1:18-25; Luke 24:36-40);<br>
He died (John 19:30-35);<br>
He rose again from the dead bodily (John 20:25-29; Luke24:36-40);<br>
He ascended into heaven bodily (1 Thessalonians 4:16-17; Matthew 29-30).<br>
We believe that the only means of being saved from sin and eternal damnation is repentance and faith in the person and sacrificial death and resurrection of Jesus Christ (Acts 3:19; Luke 24:47; Rome 10:9; Ephesians 1:7; Hebrews 2:3).<br>
We believe in both the sovereignty of God and inescapable human responsibility in all life and faith. We believe that except by the express effectual design of God, no flesh can exist nor be saved; and except humans believe, they shall likewise perish. These are not contradictory but parallel truth (antimony), with the sovereignty of God having the initiatory and override might. For no one can resist the will of God (Romans 9:19).<br>
We believe that all those in every time and town who have been redeemed through the merits and their personal belief in the Christ event (birth, life, suffering, death, resurrection and ascension) are members of His own body known as the Church-irrespective of denomination (1 Cor. 12:12-14, 27) and are preserved by God forever.
We believe regeneration by the power of the Holy Spirit through the Word of God is essential for personal salvation. (Titus 2:5, John 3:3-5; Ephesians 5:25-27)<br>
We believe that the redemptive work of Christ on the cross provides divine healing for the body, salvation for the soul of everyone that believes. (1 Peter 2:24; Acts 3:16; Acts 9:32-35).<br>
We believe that when an individual receives the Holy Ghost, he receives divine enablement for Christian service and witness. We believe that no true believer is without the Holy Spirit. (Acts 1:8; 2:4; 3:1-26; 4:5-12; Romans 8:9, 14, 16).<br>
We believe in the positional, processional and perfect sanctifying power of the Holy Ghost in the life of every believer (Romans 15:16; 1 Cor. 6:11).<br>
We believe in the final resurrection of both the saved and the lost; the former to eternal life and the latter to eternal judgment. We believe in a literal heaven and hell that will soon be the eternal destiny of human kind depending on what each one does with Jesus Christ. (Rev. 20:11-15; 1 Cor. 15:23)<br>
